
public class VariaveisCharString {
	public static void main(String[] args){
		System.out.println("Vasco da Gama!!!");
		
		//char letra1 = 'V', letra2 = 'a', letra3 = 's', letra4 = 'c', letra5 = 'o';
		
		//Outra forma de declarar uma variavel:
		char letra1 = 'V';
		char letra2 = 'a';
		char letra3 = 's';
		char letra4 = 'c';
		char letra5 = 'o';
		
		System.out.println(letra1);
		System.out.println(letra2);
		System.out.println(letra3);
		System.out.println(letra4);
		System.out.println(letra5);
		
		char letra = 65;
		System.out.println(letra);
		letra = (char) (letra +1);
		System.out.println(letra);
		
		String vasco = "Vasco da ";
		String gama = "Gama!!!";
		
		System.out.println(vasco + gama);
		}
	}
